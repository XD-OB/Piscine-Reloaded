int		ft_sqrt(int nb)
{
	int		i;

	if (nb == 0 || nb == 1)
		return (nb);
	i = 1;
	while ((i * i) < nb)
		i++;
	if ((i * i) == nb)
		return (i);
	else
		return (0);
}
